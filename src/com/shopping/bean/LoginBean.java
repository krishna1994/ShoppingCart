package com.shopping.bean;

public class LoginBean
{
	private String username; 
	private String password;
	
	
	public String getUsername() 	// To get the value of username
	{
		return username;   
	}
	public void setUsername(String username) //To set the value of Username
	{
		this.username = username;
	}
	public String getPassword() {	// To get the value of password
		return password;
	}
	public void setPassword(String password) 	// To set the value of password
	{ 	
		this.password = password;
	}
	
	

	

}
